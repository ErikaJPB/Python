### Type Hints ###

my_string_variable = "Hello World"
print(my_string_variable)   # Prints "Hello World"
print(type(my_string_variable)) # Prints <class 'str'>

  
my_string_variable = 5
print(my_string_variable)   # Prints 5
print(type(my_string_variable)) # Prints <class 'int'>


my_typed_variable: str = "Hello World"
print(my_typed_variable)   # Prints "Hello World"
print(type(my_typed_variable)) # Prints <class 'str'>


my_typed_variable = 5
print(my_typed_variable)   # Prints 5
print(type(my_typed_variable)) # Prints <class 'int'>